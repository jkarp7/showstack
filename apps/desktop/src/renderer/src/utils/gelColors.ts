/**
 * Get hex color for a gel code (e.g., "L202", "R119", "L202+R119")
 * Returns array of hex colors for dual colors, or null if not found
 */
export function getGelColor(gelCode: string | null | undefined): string[] | null {
  if (!gelCode) return null;

  // Handle dual colors (e.g., "L202+R119")
  const dualMatch = gelCode.match(/^([^+]+)\+(.+)$/);
  if (dualMatch) {
    const [, color1, color2] = dualMatch;
    const hex1 = gelColors[color1.trim().toUpperCase()];
    const hex2 = gelColors[color2.trim().toUpperCase()];
    if (hex1 && hex2) {
      return [hex1, hex2];
    }
  }

  // Single color lookup
  const hex = gelColors[gelCode.trim().toUpperCase()];
  return hex ? [hex] : null;
}

/**
 * Get manufacturer from gel code prefix
 */
export function getGelManufacturer(gelCode: string): string {
  const code = gelCode.trim().toUpperCase();
  if (code.startsWith('G')) return 'GAM';
  if (code.startsWith('L')) return 'LEE';
  if (code.startsWith('R')) return 'Roscolux';
  return 'Unknown';
}

/**
 * Split dual gel codes into individual codes
 */
export function splitDualGelCode(gelCode: string): string[] {
  const dualMatch = gelCode.match(/^([^+]+)\+(.+)$/);
  if (dualMatch) {
    return [dualMatch[1].trim(), dualMatch[2].trim()];
  }
  return [gelCode.trim()];
}
